// Conditions

// If - Else
let signal = "Red"

// Condition 1
if (signal == "Red") {
    console.log("Red => Stop");
}
// Condition 2
else if (signal == "Yellow") {
    console.log("Yellow => Go Slow");
}
// Condition 3
else if (signal == "Green") {
    console.log("Green => Go Fast");
}
// Condition 4
else {
    console.log("Invalid");
}